<p><strong>I.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong><strong>WNI Berdomisili di Indonesia</strong></p>
<ol>
  <li>Bagi warga negara Indonesia yang berdomisili atau berada di wilayah Indonesia,&nbsp;permohonan Paspor biasa diajukan kepada Menteri atau Pejabat Imigrasi yang ditunjuk pada kantor Imigrasi setempat dengan mengisi aplikasi data dan melampirkan dokumen kelengkapan persyaratan yang terdiri atas :
      <ol>
        <li>kartu tanda penduduk yang masih berlaku atau surat keterangan pindah keluar negeri;</li>
        <li>kartu keluarga;</li>
        <li>akta kelahiran, akta perkawinan atau buku nikah, ijazah, atau surat baptis;</li>
        <li>surat pewarganegaraan Indonesia bagi Orang asing yang memperoleh kewarganegaraan Indonesia melalui pewarganegaraan atau penyampaian pernyataan untuk memilih kewarganegaraan sesuai dengan ketentuan peraturan perundang-undangan;</li>
        <li>surat penetapan ganti nama dari pejabat yang berwenang bagi yang telah mengganti nama; dan</li>
        <li>Paspor biasa lama bagi yang telah memiliki paspor biasa.&nbsp;</li>
      </ol>
  </li>
  <li>Dokumen kelengkapan persyaratan sebagaimana dimaksud pada poin 1 huruf c harus dokumen yang memuat :
      <ol>
        <li>nama;</li>
        <li>tanggal lahir;</li>
        <li>tempat lahir; dan</li>
        <li>nama orang tua&nbsp;</li>
      </ol>
  </li>
  <li>Dalam hal dokumen sebagaimana dimaksud pada poin 1 huruf c tidak memuat data sebagaimana dimaksud pada poin 2, permohonan dapat melampirkan surat keterangan dari instansi yang berwenang.</li>
</ol>
<p><strong>II.&nbsp;&nbsp;&nbsp;&nbsp; Anak WNI Berdomisili di Indonesia</strong></p>
<ol>
  <li>Bagi anak warga negara Indonesia yang berdomisili atau berada di wilayah Indonesia, permohonan Paspor biasa diajukan kepada Menteri atau Pejabat Imigrasi yang ditunjuk pada Kantor Imigrasi setempat dengan mengisi aplikasi data dan melampirkan persyaratan:
      <ol>
        <li>Kartu tanda penduduk ayah atau ibu yang masih berlaku atau surat keterangan pindah ke luar negeri;</li>
        <li>Kartu keluarga;</li>
        <li>Akta kelahiran atau surat baptis</li>
        <li>Akta perkawinan atau buku nikah orangtua;</li>
        <li>Surat penetapan ganti nama dari pejabat yang berwenang bagi yang telah mengganti nama; dan</li>
        <li>Paspor biasa lama bagi yang telah memiliki paspor biasa</li>
      </ol>
  </li>
</ol>
<p>&nbsp;<strong>III.&nbsp;&nbsp;&nbsp;&nbsp;</strong><strong>Calon TKI Domisili Indonesia</strong></p>
<ol>
  <li>Bagi calon tenaga kerja Indonesia yang berdomisili atau berada di wilayah Indonesia, permohonan paspor biasa diajukan kepada Menteri atau Pejabat Imigrasi yang ditujukan pada kantor Imigrasi yang masih berada dalam Provinsi yang sama dengan domisili yang bersangkutan.</li>
  <li>Permohonan sebagaimana dimaksud pada poin 1 dilakukan dengan mengisi aplikasi data dan melampirkan persyaratan:
      <ol>
        <li>Kartu tanda penduduk yang masih berlaku atau surat keterangan pindah ke luar negeri;</li>
        <li>Kartu keluarga;</li>
        <li>Akta kelahiran, akta perkawinan atau buku nikah, ijazah, atau surat baptis;</li>
        <li>Surat pewarganegaraan Indonesia bagi Orang Asing yang memperoleh kewarganegaraan Indonesia melalui pewarganegaraan atau penyampaian pernyataan untuk memilih kewarganegaraan sesuai dengan ketentuan peraturan perundang-undangan;</li>
        <li>Surat penetapan ganti nama pejabat yang berwenang bagi yang telah mengganti nama;</li>
        <li>Surat rekomendasi permohonan paspor Calon Tenaga Kerja Indonesia yang diterbitkan oleh Dinas Tenaga Kerja Provinsi atau kabupaten/kota; dan</li>
        <li>Paspor biasa lama, bagi yang telah memiliki Paspor biasa.</li>
      </ol>
  </li>
  <li>Dokumen kelengkapan persyaratan sebagaimana dimaksud pada poin 2 huruf c harus dokumen yang memuat:
      <ol>
        <li>Nama;</li>
        <li>Tanggal lahir;</li>
        <li>Tempat lahir; dan</li>
        <li>Nama orang tua.</li>
      </ol>
  </li>
  <li>Dalam hal dokumen sebagaimana dimaksud pada poin 2 huruf c tidak memuat data sebagaimana dimaksud pada poin 3, Pemohon dapat melampirkan surat keterangan dari instansi yang berwenang.</li>
</ol>
<p><strong>IV. WNI Domisili Luar Indonesia</strong></p>
<p>Bagi warga negara Indonesia yang berdomisili di luar wilayah Indonesia, permohonan Paspor biasa diajukan kepada Menteri atau Pejabat Imigrasi yang ditunjuk pada Perwakilan Republik Indonesia dengan mengisi aplikasi data dan melampirkan persyaratan:</p>
<ol>
  <li>Kartu penduduk negara setempat, bukti, petunjuk, atau keterangan yang menunjukkan bahwa pemohon bertempat tinggal di negara tersebut; dan</li>
  <li>Paspor biasa lama.</li>
</ol>
<p><strong>V. Anak Berkewarganegaraan Indonesia yang Lahir di Luar Indonesia</strong></p>
<p>Bagi anak berkewarganegaraan Indonesia yang lahir di luar wilayah Indonesia, permohonan Paspor biasa di luar Wilayah Indonesia diajukan kepada Menteri atau Pejabat Imigrasi pada Perwakilan Republik Indonesia dengan melampirkan persyaratan:</p>
<ol>
  <li>Paspor biasa ayah dan/atau ibu warga negara Indonesia; dan</li>
  <li>Surat keterangan lahir dari Perwakilan Republik Indonesia.</li>
</ol>

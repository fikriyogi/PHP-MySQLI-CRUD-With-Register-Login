  </li>
  <li><strong><strong>Penarikan Paspor Biasa</strong></strong>
      <ol>
        <li>Penarikan paspor biasa dapat dilakukan kepada pemegangnya pada saat berada di dalam atau berada di luar wilayah Indonesia;</li>
        <li>Penarikan paspor biasa sebagaimana dimaksud pada poin 1 dilakukan dalam hal:
            <ol>
              <li>Pemegangnya telah dinyatakan sebagai tersangka oleh instansi berwenang atas perbuatan atas perbuatan pidana yang diancam hukuman paling singkat 5 (lima) tahun atau&nbsp;<em>red notice</em>&nbsp;yang telah berada diluar wilayah Indonesia; atau</li>
              <li>Masuk dalam daftar pencegahan;</li>
            </ol>
        </li>
        <li>Penarikan paspor biasa di wilayah Indonesia dilakukan oleh Kepala kantor Imigrasi atau pejabat Imigrasi yang ditunjuk;</li>
        <li>Penarikan paspor biasa di luar wilayah Indonesia dilakukan oleh pejabat imigrasi yang&nbsp; ditunjuk pada perwakilan Republik Indonesia;</li>
        <li>Dalam hal Perwakilan Republik Indonesia belum ada pejabat Imigrasi sebagaimana dimaksud pada poin 3, penarikan paspor biasa dilakukan oleh Pejabat Dinas Luar Negeri;</li>
        <li>Dalam hal penarikan paspor biasa dilakukan pada saat pemegangnya berada di luar wilayah Indonesia sebagaimana dimaksud dalam poin 4, kepada yang bersangkutan diberikan Surat Perjalanan Laksana Papor sebagai dokumen pengganti yang akan digunakan untuk proses pemulangan;</li>
        <li>Penarikan paspor biasa sebagaimana dimaksud dalam poin 3 dilakukan dengan penyampaian surat pemberitahuan penarikan Papor Biasa kepada pemegangnya;</li>
        <li>Pemegang paspor biasa yang mendapatkan surat pemberitahuan sebagaimana dimaksud pada poin 7 wajib menyerahkan paspor biasa kepada pejabat Imigrasi yang ditunjuk dalam waktu paling lama 3 (tiga) hari;</li>
        <li>Dalam hal pemegang Paspor biasa tidak menyerahkan dalam waktu sebagaimana dimaksud pada poin 8, Pejabat Imigrasi yang ditunjuk harus menarik langsung Paspor biasa dari pemegangnya.;</li>
        <li>Paspor biasa dapat diberikan kembali kepada pemegangnya oleh Pejabat Imigrasi yang ditunjuk, dalam hal:
            <ol>
              <li>Tidak terbukti melakukan perbuatan pidana berdasarkan keputusan pengadilan yang berkekuatan hukum tetap;</li>
              <li><em>Red notice&nbsp;</em>dicabut oleh interpol; atau</li>
              <li>Namanya dicabut dari daftar pencegahan.</li>
            </ol>
        </li>
      </ol>
  </li>
</ol>

  <li><strong><strong>Perubahan Data</strong></strong>
      <ol>
        <li>Dalam hal terjadi perubahan data pemegang paspor biasa yang meliputi perubahan nama&nbsp; atau perubahan alamat pemohon dapat mengajukan permohonan perubahan data paspor biasa kepada kepala kantor Imigrasi atau Pejabat Imigrasi</li>
        <li>Prosedur perubahan data paspor biasa, dilaksanakan melalui tahapan :
            <p>a)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Pengajuan permohonan</p>
          <p>b)&nbsp;&nbsp;&nbsp;&nbsp; Persetujuan kepala Kantor imigrasi atau Pejabat imigrasi dan</p>
          <p>c)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Pencetakan perubahan data pada halaman pengesahan</p>
        </li>
      </ol>

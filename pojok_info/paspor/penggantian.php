<ol>
  <li>Penggantian paspor biasa dilakukan jika;
      <ol>
        <li>Masa berlakunya akan atau telah habis;</li>
        <li>Halaman penuh;</li>
        <li>Hilang;</li>
        <li>Rusak pada saat;
            <ol>
              <li>Proses penerbitan; atau</li>
              <li>Di luar proses penerbitan, sehingga keterangan di dalamnya menjadi tidak jelas atau memberi kesan yang tidak pantas lagi sebagai dokumen resmi.</li>
            </ol>
        </li>
      </ol>
  </li>
  <li>Penggantian Paspor biasa yang masa berlakunya akan atau telah habis, halaman penuh, atau rusak pada saat di luar proses penerbitan sebagaimana dimaksud pada poin 1 huruf d angka 2 ditindaklanjuti dengan pencabutan;</li>
  <li>Penggantian Paspor biasa yang rusak pada saat proses penerbitan sebagaimana dimaksud pada poin 1 huruf d angka 1 ditindaklanjuti dengan pembatalan;</li>
  <li>Penggantian Paspor biasa dapat diberikan di wilayah Indonesia atau di luar wilayah Indonesia;</li>
  <li>Penggantian Paspor biasa yang diajukan di wilayah Indonesia diberikan oleh Kepala Kantor Imigrasi atau Pejabat Imigrasi yang ditunjuk;</li>
  <li>Penggantian Paspor biasa yang diajukan di luar wilayah Indonesia diberikan oleh Pejabat Imigrasi yang ditunjuk pada perwakilan Republik Indonesia;</li>
  <li>Dalam hal pada perwakilan Republik Indonesia belum ada Pejabat Imigrasi sebagaimana dimaksud pada poin 5, penggantian Paspor biasa dilakukan oleh Pejabat Dinas Luar Negeri;</li>
  <li>Penggantian Paspor biasa sebagaimana dimaksud dalam poin 1 huruf a, huruf b, dan huruf d angka 1 yang diajukan di wilayah Indonesia dapat langsung diberikan penggantian Paspor biasa melalui prosedur permohonan Paspor biasa;</li>
  <li>Penggantian Paspor biasa sebagaimana dimaksud dalam poin 1 huruf d angka 2 yang diajukan di Wilayah Indonesia, diberikan setelah pembuatan berita acara pemeriksaan dan mendapat persetujuan Kepala Kantor Imigrasi atau Pejabat Imigrasi yang ditunjuk;</li>
  <li>Penggantian Paspor biasa sebagaimana dimaksud dalam poin 1 huruf a, huruf b, dan huruf d angka 1 yang diajukan di luar Wilayah Indonesia dapat dilakukan penggantian Paspor Biasa melalui prosedur permohonan di luar wilayah Indonesia;</li>
  <li>Penggantian Paspor biasa sebagaimana dimaksud dalam poin 1 huruf d angka 2 yang diajukan di luar Wilayah Indonesia, diberikan setelah pembuatan berita acara pemeriksaan dan mendapat persetujuan Kepala Perwakilan Republik Indonesia;</li>
  <li>Berita acara pemeriksaan penggantian Paspor biasa sebagaimana dimaksud dalam poin 9 dilaksanakan oleh Pejabat Imigrasi yang ditunjuk atau Pejabat Dinas Luar Negeri dalam waktu paling lama 2 (dua) hari sejak diterimanya permohonan penggantian Paspor biasa.;</li>
  <li>Dalam hal permohonan penggantian Paspor biasa disetujui, Pejabat Imigrasi yang ditunjuk atau Pejabat Dinas Luar Negeri dapat melanjutkan proses penggantian Paspor biasa dalam waktu paling lama 3 (tiga) hari sejak diterimanya permohonan penggantian Paspor biasa;</li>
  <li>Permohonan penggantian Paspor biasa sebagaimana dimaksud dalam poin 1 huruf c diajukan secara langsung oleh pemohon dengan mengisi aplikasi data dan melampirkan persyaratan:
      <ol>
        <li>Surat lapor kehilangan dari kepolisian setempat;</li>
        <li>Kartu tanda penduduk yang masih berlaku; dan</li>
        <li>Kartu keluarga.</li>
      </ol>
  </li>
  <li>Pejabat Imigrasi melakukan pemeriksaan terhadap permohonan penggantian Paspor biasa sebagaimana dimaksud pada poin 14 dan dituangkan dalam Berita Acara Pemeriksaan;</li>
  <li>Berita acara pemeriksaan Paspor biasa sebagaimana dimaksud pada poin 15 dilaksanakan&nbsp; oleh Pejabat Imigrasi yang ditunjuk dalam waktu paling lama 1 (satu) hari terhitung sejak tanggal diterimanya permintaan pengajuan penggantian Paspor biasa;</li>
  <li>Berita acara pemeriksaan Paspor biasa disampaikan kepada Kepala Kantor Imigrasi untuk mendapatkan pertimbangan dalam memberikan keputusan penggantian;</li>
  <li>Pemberian keputusan penggantian Paspor biasa sebagaimana dimaksud pada poin 17 dilaksanakan dalam waktu paling lama 1 (satu) hari terhitung sejak dimintakan pertimbangan.</li>
  <li>Dalam hal Kepala Kantor Imigrasi menyetujui penggantian Paspor biasa, Pejabat Imigrasi mengeluarkan penggantian Paspor biasa;</li>
  <li>Dalam hal dari hasil pemeriksaan diperoleh petunjuk Paspor biasa hilang atau rusak&nbsp; sebagaimana dimaksud dalam poin 1 huruf c dan huruf d disebabkan karena:
      <ol>
        <li>Musibah yang dialami oleh yang bersangkutan antara lain kebakaran, kebanjiran dan gempa bumi, dapat diberikan penggantian langsung;</li>
        <li>Ditemukan adanya unsur kekurang hati-hatian dan terjadinya kehilangan di luar kemampuan pemegang Paspor biasa, diberikan penggantian Paspor biasa;</li>
        <li>Ditemukan adanya unsur kecerobohan atau kelalaian disertai alasan yang tidak dapat diterima, pemberian Paspor biasa dapat ditangguhkan paling sedikit 6 (enam) bulan sampai dengan paling lama 2 (dua) tahun.</li>
      </ol>
  </li>
  <li>Penggantian Paspor biasa karena hilang atau rusak sebagaimana dimaksud pada poin 20 dikenakan biaya denda sebagai berikut:
      <ol>
        <li>Disebabkan karena musibah, dibebaskan dari pengenaan biaya;</li>
        <li>Disebabkan karena kekurang hati-hatian tanpa unsur kesengajaan, dikenakan denda sebesar biaya Paspor biasa yang hilang ata rusak; dan</li>
        <li>Disebabkan karena kecerobohan, dikenakan denda 2 (dua) kali lipat dari biaya Paspor biasa yang hilang atau rusak.</li>
      </ol>
  </li>
  <li>Pejabat Imigrasi yang ditunjuk atau Pejabat Dinas Luar Negeri dapat menolak permohonan penggantian Paspor biasa karena alasan tertentu</li>
  <li>Alasan tertentu sebagaimana dimaksud pada poin 22 meliputi :
      <ol>
        <li>Tempat tinggal pemohon berada di luar wilayah akreditasi perwakilan Republik Indonesia di luar negeri; atau</li>
        <li>Pemohon telah kehilangan kewarganegaraan Republik Indonesia dan/atau telah memperoleh kewarganegaraan lain.</li>
      </ol>
  </li>
  <li>Pejabat Imigrasi yang ditunjuk atau Pejabat Dinas Luar Negeri menyampaikan penolakan pemberian Paspor biasa kepada pemohon dalam jangka waktu paling lama 1 (satu) hari terhitung sejak tanggal diterimanya permohonan penggantian Paspor biasa.</li>
</ol>
